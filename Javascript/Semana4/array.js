// push(): Añade un elemento al final del array.
let nombres = ["Cesar", "David"]
nombres.push("Zoe")
console.log(nombres)

// pop(): Elimina el último elemento del array.
nombres.pop()
console.log(nombres)

// shift(): Elimina el primer elemento del array.
nombres.shift()
console.log(nombres)

// unshift(): Añade un elemento al inicio del array.
nombres.shift("Zoe")
console.log(nombres)

// sort(): Ordena los elementos del array.
let letras = ["a", "b", "g", "c", "Y", "y", "j"]
console.log(letras)
nombres.sort()
console.log(letras)

// reverse(): Invierte el orden de los elementos en el array. 
letras.reverse()
console.log(letras)